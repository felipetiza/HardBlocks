function crearMapa(mapa, width){
	for(var i = 0; i < mapa.length; i++) {			// i = fila   j = columna
		for(var j = 0; j < mapa[i].length; j++) {
			if(mapa[i][j] == 5)
				bloqueMarron.push(new Figura(j*width,i*width,width,width,null,null));
			else if(mapa[i][j] == 4)
				bloqueRojo.push(new Figura(j*width,i*width,width,width,null,null));
			else if(mapa[i][j] == 3)
				bloqueBlancoVert.push(new Figura(j*width,i*width,width,width,null,null));
			else if(mapa[i][j] == 2)
				bloqueBlancoHor.push(new Figura(j*width,i*width,width,width,null,null));
		}
	}
}
